          <h4 class="service-heading">Sejarah Kota Pekalongan</h4>
          <div class="justify">
          <p class="text-muted text_content"> Asal mula nama Pekalongan berasal dari Topo Ngalongnya Joko Bau /Jaka Bau putra dari Kyai Cempaluk pahlawan Mataram dari Kesesi. Suatu hari, Joko Bau diperintahkan oleh Ki Cempaluk untuk mengabdi pada Sultan Agung raja Mataram. Dia juga mendapat tugas untuk memboyong putri Ratnasari Kalisasak Batang ke istana, tetapi Joko Bau malah jatuh cinta pada sang putri.</p>
          <p class="text-muted text_content">Karena tindakannya mencintai putri Ratnasari, ia diberi hukuman untuk mengamankan daerah pesisir yang dibajak oleh Cina. Kemudian Joko Bau bersemedi di hutan Gambiran, setelah lama ia bersemedi akhirnya namanya berganti manjadi Bau Rekso. Atas perintah Sultan Agung, Bau Rekso mempersiapkan pasukan untuk menggempur para kompeni yang berada di Batavia (1628-1629).</p>
          <p class="text-muted text_content">Namun, serangan itu mengalamai kegagalan, kemudian ia kembali ke hutan Gambiran untuk bertapa "ngalong" artinya bergelantungan seperti kelelawar. Saat topo ngalong Joko Bau/Bau Rekso berlangsung, ia pernah sekali diganggu oleh Tan Kwie Djan atas perintah dari Mataram untuk menerima tugas. Dari nama asal topo ngalong inilah kemudian menjadi nama Pekalongan. Hingga pada akhirnya, karena memperoleh kekuatan gaib, Dewi Lanjar mau dipersunting oleh di Bau Rekso.
          Sedangkan munculnya nama Pekalongan menurut versi abad XVII adalah di masa Sultan Agung saat Ki Bau Rekso gugur saat melawan pada tanggal 21 September 1628 melawan VOC di Batavia. Tempat topo ngalong dari Joko Bau berada di Wiradesa, Kesesi, Slamaran, Ulujami, Comal dan Alun-alun Pekalongan.</p>
