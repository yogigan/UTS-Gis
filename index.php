          <div class="justify">
          <p class="text-muted">Secara geografis, wilayah Kota Pekalongan terletak antara 60 50’ 42" - 60 55’ 44” Lintang Selatan dan 1090 37’ 55” - 1090 42’ 19” Bujur Timur. Rencana Tata Ruang Wilayah Nasional (RTRWN) menetapkan Kota Pekalongan sebagai Pusat Kegiatan Wilayah (PKW). Sebagai PKW maka diharapkan Kota Pekalongan dapat berperan menjadi pusat pengembangan bagi wilayah di sekitarnya, yang meliputi Kabupaten Pekalongan dan Kabupaten Batang.</p>

         <p class="text-muted text_content">Dalam sistem pengembangan wilayah Provinsi Jawa Tengah, RTRW Provinsi Jawa Tengah juga menetapkan Kota Pekalongan sebagai bagian dan simpul utama dari Kawasan Petanglong (Kawasan Kota Pekalongan, Kabupaten Batang dan Kabupaten Pekalongan). Kawasan Petanglong adalah kawasan strategis dari sudut kepentingan pertumbuhan ekonomi dan sektor unggulannya adalah pertanian, pariwisata, industri dan perikanan. Potensi ekonomi yang manjadi andalan Kawasan Petanglong meliputi sektor primer adalah perikanan; sektor sekunder adalah tekstil, batik, dan pengolahan ikan; serta sektor tersier adalah jasa dan perdagangan. Kondisi ini tentunya menjadikan Kota Pekalongan memiliki posisi yang sangat strategis.</p>

        <p class="text-muted text_content">Sebagai daerah yang telah berkembang dan produk domestik regional bruto tahun 2016 terbesarnya disumbangkan lapangan usaha Perdagangan Besar dan Eceran, Reparasi Mobil dan Sepeda Motor (21,72%), Industri Pengolahan (21,43%), dan Konstruksi (14,36%) serta posisi strategis Kota Pekalongan di jalur Pantai Utara Jawa maka tentunya Kota Pekalongan memiliki keunggulan komparatif (comparative advantage) dibandingkan daerah lainnya. Diharapkan keunggulan ini dapat menjadi lokomotif bagi kemajuan dan peningkatan kesejahteraan masyarakat secara berkelanjutan.</p>
